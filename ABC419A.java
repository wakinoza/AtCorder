
import java.util.*;

public class Main {
      public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String s = scanner.next();
        scanner.close();
        switch (s) {
            case "red" ->{
                System.out.println("SSS");
            }
            case "blue" ->{
                System.out.println("FFF");
            }
            case "green" ->{
                System.out.println("MMM");
            }
            default ->{
                System.out.println(" Unknown");
            }
        }
    }
} 


